/* Code challenge: Implement a method of the Math class that returns the larger of two numbers
    int firstValue = 500;
    int secondValue = 600;
    int largerValue;
    Console.WriteLine(largerValue);*/

int firstValue = 500;
int secondValue = 600;
int largerValue = Math.Max(firstValue, secondValue);

Console.WriteLine(largerValue);  // 600
